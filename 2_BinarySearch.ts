const target_Value = 722;

function binarySearch(arr:Array<number>, target:number):boolean{
    let high = arr.length;
    let low = 0;
    do{
        const mid_value = Math.floor(low+(high-low)/2)
        const arr_mid_value = arr[mid_value]
        if(arr_mid_value===target){
            return true
        }else if(arr_mid_value>target){
            high=mid_value+1
        }else{
            low=mid_value
        }
    }while(low<high)
    return false
}

